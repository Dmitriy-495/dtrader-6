// Этот файл содержит вспомогательные функции для работы со временем.
// Gate.io требует timestamp в секундах Unix в каждом подписанном запросе.
package utils

import (
	// time — стандартная библиотека Go для работы со временем.
	// Содержит всё необходимое: текущее время, форматирование, таймеры.
	"time"
)

// NowUnix возвращает текущее время в секундах Unix (int64).
//
// Unix timestamp — это количество секунд прошедших с 1 января 1970 года.
// Gate.io использует именно этот формат для timestamp в подписях.
//
// Пример возвращаемого значения: 1741523200
//
// Почему int64 а не int?
// int на 32-битных системах = 32 бита, максимум ~2 млрд.
// Unix timestamp уже превысил 1.7 млрд и продолжает расти.
// int64 = 64 бита, максимум ~9.2 квинтиллиона — хватит до года 292 млрд. 😄
func NowUnix() int64 {
	// time.Now() возвращает текущее время типа time.Time.
	// .Unix() конвертирует time.Time в int64 секунд Unix.
	return time.Now().Unix()
}

// NowUnixMilli возвращает текущее время в миллисекундах Unix (int64).
//
// Используется для высокоточных временных меток в тиках и стакане.
// LTF Sniper работает с миллисекундной точностью — секунд недостаточно.
//
// Пример возвращаемого значения: 1741523200123
// TODO: используется в publisher/redis.go (временные метки тиков и стакана)
func NowUnixMilli() int64 {
	// .UnixMilli() — аналог .Unix() но возвращает миллисекунды.
	// Добавлен в Go 1.17, у нас Go 1.22 — всё в порядке.
	return time.Now().UnixMilli()
}
